def engine_row_label(result: dict) -> str:
    """Human-facing table row label for one result.

    The two vLLM baseline arms must be tellable apart at a glance, so a vLLM
    result is labeled from its own recorded ``engine_config.vllm_version``
    ("vllm 0.9.2" / "vllm 0.24.0") - the version the measuring venv actually
    had installed, never the file or registry name. Everything else (and a
    vLLM result whose version capture failed) falls back to the registry
    engine name.

    >>> engine_row_label({"engine": "vllm", "engine_config": {"vllm_version": "0.9.2"}})
    'vllm 0.9.2'
    >>> engine_row_label({"engine": "vllm024", "engine_config": {"vllm_version": "0.24.0"}})
    'vllm 0.24.0'
    >>> engine_row_label({"engine": "vllm024", "engine_config": {"vllm_version": None}})
    'vllm024'
    >>> engine_row_label({"engine": "redline", "engine_config": {"module_version": "0.1.0"}})
    'redline'
    """
    engine = result["engine"]
    version = result.get("engine_config", {}).get("vllm_version")
    if engine.startswith("vllm") and version:
        return f"vllm {version}"
    return engine


def _label_collision(results_by_engine: dict, result: dict) -> dict | None:
    """The already-collected result (different engine name) whose row label
    would collide with ``result``'s, or None. Two arms rendering the same
    label (e.g. an engine="vllm" file measured under the 0.24 venv next to
    the formal vllm024 arm) would make the table ambiguous about what was
    measured - refused, never disambiguated silently."""
    label = engine_row_label(result)
    for other in results_by_engine.values():
        if other["engine"] != result["engine"] and engine_row_label(other) == label:
            return other
    return None


def format_cell(entry: dict, metric: str, flagged: bool) -> str:
    """One table cell: median with min-max, dispersion-flagged with a dagger.

    >>> format_cell({"value": 0.512, "min": 0.5, "max": 0.52}, "ttft_p50_s", False)
    '512.0 (500.0-520.0)'
    >>> format_cell({"value": 3210.5, "min": 3190.0, "max": 3290.2}, "throughput_tok_s", True)
    '3210.5 (3190.0-3290.2) †'
    """
    scale = 1000.0 if metric in _MS_METRICS else 1.0
    text = (
        f"{entry['value'] * scale:.1f} "
        f"({entry['min'] * scale:.1f}-{entry['max'] * scale:.1f})"
    )
    return f"{text} †" if flagged else text


def _sort_key(sequence: tuple, value: str) -> tuple:
    return (sequence.index(value) if value in sequence else len(sequence), value)


def _reproduction_lines(results: list[dict]) -> list[str]:
    """The reproduction section: harness prerequisites (pointers only - the
    pinned versions, hashes, and launch lines live in bench/FAIRNESS.md) plus
    the exact recorded command line of every rendered run, verbatim from its
    results file's ``environment.command_line``. Commands are static text or
    sourced from the results files; no measured value is written here."""
    recorded = {
        (result["case"], result["engine"]): " ".join(
            str(part) for part in result["environment"]["command_line"]
        )
        for result in results
    }
    ordered = sorted(
        recorded,
        key=lambda key: (_sort_key(CASE_ORDER, key[0]), _sort_key(ENGINE_ORDER, key[1])),
    )
    return [
        "### Reproducing these numbers",
        "",
        "1. Build the engine (presets in `CMakePresets.json`): "
        "`cmake --preset pod-release && cmake --build --preset pod-release`, "
        "then `ctest --preset pod-release`.",
        "2. Fetch the pinned model checkpoint: `python scripts/fetch_model.py` "
        "(revision pin documented in `docs/MODEL_SPEC.md`).",
        "3. Set up the baselines exactly as pinned in `bench/FAIRNESS.md` "
        '("Engine configuration"): each vLLM arm from its own freeze file in '
        "its own virtual environment (`bench/requirements.lock` for the "
        "`vllm` rows, `bench/requirements-vllm024.lock` for the `vllm024` "
        "rows); llama.cpp built at the recorded commit, the FP16 GGUF "
        "conversion with its recorded hash, and the recorded `llama-server` "
        "launch line (per-case context sizing in the same file).",
        "4. Verify the cross-language workload identity, then run the "
        "output-equivalence gate before measuring anything:",
        "",
        "   ```",
        "   python -m bench.workload --selftest   # must equal: redline_bench --selftest-workload",
        "   python -m bench.run_bench --equivalence-gate --engines redline,vllm,llamacpp \\",
        "       --model-dir <model_dir> --out bench/results/equivalence/transcript.json",
        "   ```",
        "",
        "5. The recorded argv of every run in the tables above, verbatim from "
        "its results file (`environment.command_line`). Paths are the benchmark "
        "machine's; `PYTHONPATH` carries the built `redline` module, each vLLM "
        "arm's rows run under that arm's pinned virtual environment's "
        "interpreter, and the llamacpp rows require the running `llama-server` "
        "from step 3:",
        "",
        "   ```",
        *(f"   {recorded[key]}" for key in ordered),
        "   ```",
        "",
        "6. Regenerate this README section from the committed results: "
        "`python -m bench.report --results bench/results/raw/ --write-readme README.md`.",
    ]


def render_markdown(results: list[dict]) -> str:
    """Results -> markdown block (tables per case + scope caption + environment
    footnote). Raises ReportError on duplicate engine/case pairs or mixed
    environments."""
    if not results:
        raise ReportError("nothing to render")
    gpu, driver = check_same_environment(results)

    by_case: dict = {}
    for result in results:
        engines = by_case.setdefault(result["case"], {})
        if result["engine"] in engines:
            raise ReportError(
                f"duplicate results for engine={result['engine']} case={result['case']} "
                f"({engines[result['engine']]['_source']} and {result['_source']})"
            )
        clash = _label_collision(engines, result)
        if clash is not None:
            raise ReportError(
                f"ambiguous row label {engine_row_label(result)!r} for "
                f"case={result['case']}: engines {clash['engine']} "
                f"({clash['_source']}) and {result['engine']} ({result['_source']}) "
                "would render identically"
            )
        engines[result["engine"]] = result

    lines: list[str] = ["## Benchmark results", ""]
    for case in sorted(by_case, key=lambda c: _sort_key(CASE_ORDER, c)):
        engines = by_case[case]
        sample = next(iter(engines.values()))
        w = sample["workload"]
        plural = "request" if w["num_requests"] == 1 else "requests"
        lines.append(
            f"### case `{case}` - {w['num_requests']} {plural}, "
            f"{w['input_len']} in / {w['output_len']} out"
        )
        lines.append("")
        headers = ["engine"] + [header for _, header in COLUMNS]
        lines.append("| " + " | ".join(headers) + " |")
        lines.append("|" + "|".join(["---"] * len(headers)) + "|")
        for engine in sorted(engines, key=lambda e: _sort_key(ENGINE_ORDER, e)):
            result = engines[engine]
            flagged = bool(result["dispersion"].get("flag"))
            row = [f"`{engine_row_label(result)}`"]
            for metric, _ in COLUMNS:
                entry = result["metrics"].get(metric)
                if entry is None:
                    row.append("-")
                else:
                    row.append(format_cell(entry, metric, flagged and metric == HEADLINE_METRIC))
            lines.append("| " + " | ".join(row) + " |")
        lines.append("")

    lines.append(f"_{SCOPE_CAPTION}_")
    lines.append("")
    if gpu is not None:
        env_note = f"Environment: {gpu}, driver {driver or 'unrecorded'}"
    else:
        env_note = "Environment: GPU capture unavailable in these results files"
    dates = _measurement_dates(results)
    if not dates:
        measured = "measurement date unrecorded"
    elif len(dates) == 1:
        measured = f"measured {dates[0]} (UTC)"
    else:
        measured = f"measured {dates[0]} to {dates[-1]} (UTC)"
    pins = "; ".join(
        _engine_versions(
            sorted(results, key=lambda r: _sort_key(ENGINE_ORDER, r["engine"]))
        )
    )
    lines.append(
        f"_{env_note}; {measured}. Engines: {pins}. Full environment capture "
        "(clocks, versions, pip freeze, exact command line) in each results JSON._"
    )
    lines.append("")
    lines.extend(_reproduction_lines(results))
    lines.append("")
    return "\n".join(lines)


def splice_readme(readme_path: str, block: str) -> None:
    """Replace the generated block between the README markers; error when the
    markers are absent (they are added deliberately, never injected)."""
    path = Path(readme_path)
    text = path.read_text(encoding="utf-8")
    if README_BEGIN not in text or README_END not in text:
        raise ReportError(
            f"{readme_path}: missing generated-results markers; add\n"
            f"{README_BEGIN}\n{README_END}\nwhere the table belongs"
        )
    head, _, rest = text.partition(README_BEGIN)
    _, _, tail = rest.partition(README_END)
    path.write_text(
        head + README_BEGIN + "\n" + block.rstrip() + "\n" + README_END + tail,
        encoding="utf-8",
        newline="\n",  # byte-identical output on every platform
    )


# --------------------------------------------------------------------------
# --check: schema + duplicate + dispersion-flag audit (the results gate)
# --------------------------------------------------------------------------


def check_results(paths: list[str]) -> int:
    """Audit raw results: every file must validate - including the raw-data
    audit that recomputes forced lengths and every metric from the committed
    per-token data (``_audit_raw_data``) - and duplicate engine/case pairs,
    ambiguous row labels, dispersion flags and environment consistency are
    reported. Duplicate pairs and label collisions are failures because
    ``render_markdown`` refuses them - a directory that passes ``--check``
    must also render. The vLLM arms are distinct engines here: (vllm, case)
    and (vllm024, case) may coexist; two files of the SAME engine/case never
    may. Returns a process exit code."""
    failures = 0
    results = []
    seen_pairs: dict[tuple, str] = {}
    seen_labels: dict[tuple, tuple] = {}
    for path in _iter_result_files(paths):
        try:
            result = json.loads(path.read_text(encoding="utf-8"))
            validate_result(result, str(path))
        except (OSError, json.JSONDecodeError, ReportError) as error:
            failures += 1
            print(f"FAIL {path}: {error}")
            continue
        result["_source"] = str(path)
        pair = (result["engine"], result["case"])
        if pair in seen_pairs:
            failures += 1
            print(
                f"FAIL {path}: duplicate results for engine={pair[0]} "
                f"case={pair[1]} (already provided by {seen_pairs[pair]}); "
                "this directory would not render"
            )
            continue
        label = engine_row_label(result)
        prior = seen_labels.get((result["case"], label))
        if prior is not None and prior[0] != result["engine"]:
            failures += 1
            print(
                f"FAIL {path}: ambiguous row label {label!r} for "
                f"case={result['case']} (engine={result['engine']} here, "
                f"engine={prior[0]} in {prior[1]}); this directory would "
                "not render"
            )
            continue
        seen_pairs[pair] = str(path)
        seen_labels[(result["case"], label)] = (result["engine"], str(path))
        results.append(result)
        dispersion = result["dispersion"]
        flag = " DISPERSION-FLAGGED" if dispersion.get("flag") else ""
        rerun = " (re-ran once)" if dispersion.get("reran") else ""
        print(
            f"ok   {path}: engine={result['engine']} case={result['case']} "
            f"trials={len(result['trials'])}{rerun}{flag}"
        )
    if results:
        try:
            check_same_environment(results)
        except ReportError as error:
            failures += 1
            print(f"FAIL environment consistency: {error}")
    flagged = sum(1 for r in results if r["dispersion"].get("flag"))
    print(
        f"[report] checked {len(results)} valid file(s), {failures} failure(s), "
        f"{flagged} dispersion-flagged"
    )
    return 1 if failures else 0


