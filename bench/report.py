def _iter_result_files(paths: list[str]) -> list[Path]:
    files: list[Path] = []
    for raw in paths:
        path = Path(raw)
        if path.is_dir():
            files.extend(sorted(path.rglob("*.json")))
        elif path.is_file():
            files.append(path)
        else:
            raise ReportError(f"no such results file or directory: {path}")
    if not files:
        raise ReportError(f"no result JSON files found under {paths}")
    return files


# Tolerance for recomputing per-trial metrics from the committed per-token
# data: run_bench computes metrics from unrounded monotonic timestamps but
# commits the per-request record rebased and rounded to 7 decimal places
# (bench/run_bench._trace_to_json), so a faithful recomputation lands within
# ~1e-7 s per timestamp - worst observed relative shift is ~2.5e-5 on the
# ms-scale ITL percentiles. The bounds below sit ~20x above that floor and
# far below anything a result-altering edit produces.
_RECOMPUTE_REL_TOL = 5e-4
_RECOMPUTE_ABS_TOL = 1e-7


def validate_result(result: dict, source: str = "<memory>") -> None:
    """Validation gate: only numbers measured and written by run_bench.py may
    render. Structural checks first, then the raw-data audit
    (``_audit_raw_data``) that recomputes the file's numbers from its own
    committed per-token data. Raises ReportError naming the file and the
    problem."""

    def fail(problem: str) -> None:
        raise ReportError(f"{source}: {problem}")

    if not isinstance(result, dict):
        fail("not a results object")
    if result.get("schema_version") != RESULTS_SCHEMA_VERSION:
        fail(
            f"schema_version {result.get('schema_version')!r} != "
            f"{RESULTS_SCHEMA_VERSION} (regenerate with bench/run_bench.py)"
        )
    for key in ("engine", "case", "workload", "engine_config", "trials", "dispersion"):
        if key not in result:
            fail(f"missing key {key!r}")
    if result.get("forced_length_ok") is not True:
        fail("forced_length_ok is not true; the trial set is void (FAIRNESS.md)")
    environment = result.get("environment")
    if not isinstance(environment, dict) or "nvidia_smi_q" not in environment:
        fail("environment capture missing (FAIRNESS.md 'Hardware and environment')")
    if "command_line" not in environment:
        fail("environment capture lacks the exact command line")
    if not result["trials"]:
        fail("no measured trials")
    metrics = result.get("metrics")
    if not isinstance(metrics, dict) or HEADLINE_METRIC not in metrics:
        fail(f"summary metrics missing {HEADLINE_METRIC!r}")
    for name, entry in metrics.items():
        for field in ("value", "min", "max", "label"):
            if field not in entry:
                fail(f"metric {name!r} missing {field!r}")
    if "prefill" not in metrics[HEADLINE_METRIC]["label"]:
        fail("headline throughput label must state that the window includes prefill")
    median_index = result.get("median_trial_index")
    if not isinstance(median_index, int) or not 0 <= median_index < len(result["trials"]):
        fail(f"median_trial_index {median_index!r} out of range")
    _audit_raw_data(result, fail)


def _audit_raw_data(result: dict, fail) -> None:
    """The tamper audit behind ``--check`` (and every render): re-derive the
    file's numbers from its own committed per-token data instead of trusting
    the self-reported flags.

    Per trial: forced output lengths straight from the token arrays
    (``forced_length_ok`` alone proves nothing - a truncated ``token_ids``
    next to a green flag must fail), token/timestamp parallelism and
    monotonicity, and every metric recomputed through the same
    ``bench.run_bench.compute_metrics`` that wrote the file (tolerance covers
    only the committed 1e-7 timestamp rounding). Across trials: the summary
    ``metrics`` block must re-derive exactly via ``summarize_metrics`` (value
    = median trial, min/max across trials), ``dispersion.values`` must be the
    trials' headline values, ``median_trial_index`` must be the median by the
    headline metric, and the ``flag``/``reran`` markers must reproduce from
    the spread rule and the trials' own re-run markers."""
    workload = result["workload"]
    for key in ("num_requests", "output_len"):
        if not isinstance(workload.get(key), int) or workload[key] <= 0:
            fail(f"workload.{key} missing or not a positive integer")
    trials = result["trials"]
    for index, trial in enumerate(trials):
        if not isinstance(trial, dict):
            fail(f"trial {index} is not an object")
        requests = trial.get("requests")
        if not isinstance(requests, list) or not requests:
            fail(
                f"trial {index} carries no raw per-request data "
                "(FAIRNESS.md commits per-token JSON for every trial)"
            )
        if len(requests) != workload["num_requests"]:
            fail(
                f"trial {index} has {len(requests)} request records; the "
                f"workload ran {workload['num_requests']}"
            )
        traces = []
        seen_ids = set()
        for request in requests:
            rid = request.get("request_id")
            token_ids = request.get("token_ids")
            token_times = request.get("token_times_s")
            if not isinstance(token_ids, list) or not isinstance(token_times, list):
                fail(f"trial {index} request {rid!r}: token_ids/token_times_s missing")
            if rid in seen_ids:
                fail(f"trial {index}: duplicate request_id {rid!r}")
            seen_ids.add(rid)
            if len(token_ids) != len(token_times):
                fail(
                    f"trial {index} request {rid!r}: {len(token_ids)} token_ids vs "
                    f"{len(token_times)} token_times_s (index-parallel by contract)"
                )
            if len(token_ids) != workload["output_len"]:
                fail(
                    f"trial {index} request {rid!r}: raw record carries "
                    f"{len(token_ids)} tokens, forced output length is "
                    f"{workload['output_len']} - forced_length_ok is contradicted "
                    "by the committed per-token data"
                )
            if any(b < a for a, b in zip(token_times, token_times[1:])):
                fail(f"trial {index} request {rid!r}: token_times_s not non-decreasing")
            traces.append(
                RequestTrace(
                    request_id=rid,
                    enqueue_time=request.get("enqueue_time_s", 0.0),
                    token_times=[float(t) for t in token_times],
                    token_ids=list(token_ids),
                )
            )
        try:
            recomputed = compute_metrics(traces)
        except (TypeError, ValueError) as error:
            fail(f"trial {index}: metrics cannot be recomputed from the raw data ({error})")
        stored = trial.get("metrics")
        if not isinstance(stored, dict) or set(stored) != set(recomputed):
            fail(
                f"trial {index}: stored metric keys "
                f"{sorted(stored) if isinstance(stored, dict) else stored!r} != "
                f"recomputed {sorted(recomputed)}"
            )
        for name, expected in recomputed.items():
            got = stored[name]
            if isinstance(expected, int):
                matches = got == expected
            else:
                matches = isinstance(got, (int, float)) and math.isclose(
                    got, expected, rel_tol=_RECOMPUTE_REL_TOL, abs_tol=_RECOMPUTE_ABS_TOL
                )
            if not matches:
                fail(
                    f"trial {index} metric {name!r}: stored {got!r} != {expected!r} "
                    "recomputed from the committed per-token data"
                )
    if result["metrics"] != summarize_metrics(trials, result["median_trial_index"]):
        fail(
            "summary metrics do not re-derive from the per-trial metrics "
            "(value = median trial, min/max across all trials, FAIRNESS.md labels)"
        )
    dispersion = result["dispersion"]
    headline_values = [t["metrics"][HEADLINE_METRIC] for t in trials]
    if dispersion.get("metric") != HEADLINE_METRIC:
        fail(f"dispersion.metric {dispersion.get('metric')!r} != {HEADLINE_METRIC!r}")
    threshold = dispersion.get("threshold_fraction")
    if not isinstance(threshold, (int, float)) or threshold <= 0:
        fail(f"dispersion.threshold_fraction {threshold!r} is not a positive number")
    if dispersion.get("values") != headline_values:
        fail("dispersion.values do not equal the trials' headline metric values")
    if result["median_trial_index"] != select_median_trial(headline_values):
        fail(
            f"median_trial_index {result['median_trial_index']} is not the median "
            f"trial by {HEADLINE_METRIC} (expected "
            f"{select_median_trial(headline_values)})"
        )
    if bool(dispersion.get("flag")) != spread_exceeds(headline_values, threshold):
        fail("dispersion.flag contradicts the spread rule recomputed over the trials")
    if bool(dispersion.get("reran")) != any(bool(t.get("dispersion_rerun")) for t in trials):
        fail("dispersion.reran contradicts the trials' dispersion_rerun markers")


def load_results(paths: list[str]) -> list[dict]:
    """Load and validate result JSON files; each result gains a ``_source``
    key naming the file it came from."""
    results = []
    for path in _iter_result_files(paths):
        try:
            result = json.loads(path.read_text(encoding="utf-8"))
        except (OSError, json.JSONDecodeError) as error:
            raise ReportError(f"{path}: unreadable results JSON ({error})") from error
        validate_result(result, str(path))
        result["_source"] = str(path)
        results.append(result)
    return results


