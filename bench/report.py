"""Render benchmark result JSON files into a markdown table.

Input: one JSON file per engine/case as written by bench/run_bench.py
(median-of-trials metrics, min-max spread, raw trials, environment capture;
schema in that module's docstring). Output: one markdown table per case -
engines as rows; output token throughput (labeled as including prefill),
per-request decode rate, TTFT p50/p99, ITL p50/p99 as columns - each cell as
median with min-max, dispersion-flagged where the spread rule fired
(bench/FAIRNESS.md "Trials, dispersion, reporting"). The two vLLM baseline
arms (engines ``vllm`` and ``vllm024``) render as separate rows labeled by
each file's own recorded version ("vllm 0.9.2" / "vllm 0.24.0", from
``engine_config.vllm_version``), and the environment footnote lists both
arms' pins when both are present.

The table caption carries the scope statement (Qwen2.5-1.5B FP16, these
workloads only; no general-superiority claim) and the environment footnote
(GPU, driver, engine versions, measurement date - the date comes from the
results' own environment timestamps). A reproduction section follows: the
harness prerequisites plus the exact recorded command line of every rendered
run, verbatim from its results file's ``environment.command_line``. Every
number is read from a validated results file - this module refuses malformed
files, results without environment capture or forced-length confirmation,
results whose numbers do not recompute from their own committed per-token
data, and result sets measured on differing GPUs. Nothing is ever typed in by
hand.

Usage::

    python -m bench.report --results bench/results/raw/ [--out table.md]
    python -m bench.report --results bench/results/raw/ --write-readme README.md
    python -m bench.report --check bench/results/raw/

``--write-readme`` replaces the block between the markers

    <!-- BENCH_RESULTS:BEGIN (generated by bench/report.py) -->
    <!-- BENCH_RESULTS:END -->

and errors if the markers are missing. ``--check`` audits a directory of raw
results and exits non-zero on any violation, so a directory that passes
``--check`` is guaranteed to render. The audit does not trust a file's own
say-so: beyond the schema gate (duplicate engine/case pairs - the two vLLM
arms are distinct engines, so (vllm, case) and (vllm024, case) coexist -
ambiguous row labels, dispersion flags, environment consistency), it
re-derives the numbers from the committed per-token data - forced output
lengths from the token arrays themselves (``forced_length_ok`` is a claim;
the raw data is the evidence), every per-trial metric via the same
``bench.run_bench.compute_metrics`` that wrote the file, and the summary /
median-trial / dispersion blocks from the per-trial metrics - so truncating
a token list, editing a median, or dropping the raw data fails the check
even when every self-reported flag stays green.
"""

