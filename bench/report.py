def parse_gpu_name(nvidia_smi_q: str) -> str | None:
    """GPU product name from `nvidia-smi -q` text.

    >>> parse_gpu_name("...\\n    Product Name                          : NVIDIA GeForce RTX 4090\\n...")
    'NVIDIA GeForce RTX 4090'
    >>> parse_gpu_name("no gpu here") is None
    True
    """
    match = re.search(r"^\s*Product Name\s*:\s*(.+?)\s*$", nvidia_smi_q, re.MULTILINE)
    return match.group(1) if match else None


def parse_driver_version(nvidia_smi_q: str) -> str | None:
    """
    >>> parse_driver_version("  Driver Version                        : 560.35.03 ")
    '560.35.03'
    """
    match = re.search(r"^\s*Driver Version\s*:\s*(.+?)\s*$", nvidia_smi_q, re.MULTILINE)
    return match.group(1) if match else None


def _environment_gpu(result: dict) -> tuple[str | None, str | None]:
    capture = result["environment"].get("nvidia_smi_q") or {}
    text = capture.get("output") if isinstance(capture, dict) else None
    if not text:
        return (None, None)
    return (parse_gpu_name(text), parse_driver_version(text))


def check_same_environment(results: list[dict]) -> tuple[str | None, str | None]:
    """All published rows must come from the same GPU model (FAIRNESS.md);
    distinct GPU models are refused outright. Also refuses sets where some
    files carry a parsable GPU capture and others do not: a result whose
    `nvidia-smi -q` capture failed is an environment wildcard, and rendering
    it next to captured results would fake a same-GPU guarantee.

    Results sharing the GPU model but measured under different driver
    versions are the disclosed cross-session two-arm case (the ``vllm024``
    arm is measured in its own pod session on the same GPU model -
    bench/FAIRNESS.md "Engine configuration" pin disclosure): the set
    renders, and the returned driver note names every driver together with
    the rows measured under it, so the footnote never fakes a single-driver
    guarantee. Returns the common (gpu, driver-note); (None, None) only when
    NO file in the set has a capture (the footnote then says so)."""
    by_gpu: dict[str, dict[str | None, list[dict]]] = {}
    missing: list[str] = []
    for result in results:
        gpu, driver = _environment_gpu(result)
        if gpu is None:
            missing.append(result["_source"])
        else:
            by_gpu.setdefault(gpu, {}).setdefault(driver, []).append(result)
    if len(by_gpu) > 1:
        detail = "; ".join(
            f"{gpu} / driver {driver}: "
            f"{', '.join(r['_source'] for r in group)}"
            for gpu, drivers in sorted(by_gpu.items())
            for driver, group in sorted(drivers.items(), key=lambda kv: str(kv[0]))
        )
        raise ReportError(f"results measured under mismatched environments - {detail}")
    if by_gpu and missing:
        raise ReportError(
            "same-environment verification impossible - these results lack a "
            f"parsable `nvidia-smi -q` GPU capture: {', '.join(missing)} "
            "(other files in the set have one; all or none must, "
            "FAIRNESS.md 'Hardware and environment')"
        )
    if not by_gpu:
        return (None, None)
    gpu, drivers = next(iter(by_gpu.items()))
    if len(drivers) == 1:
        return (gpu, next(iter(drivers)))

    def _group_order(item: tuple) -> tuple:
        _, group = item
        return min(_sort_key(ENGINE_ORDER, r["engine"]) for r in group)

    parts = []
    for driver, group in sorted(drivers.items(), key=_group_order):
        labels = []
        for r in sorted(group, key=lambda r: _sort_key(ENGINE_ORDER, r["engine"])):
            label = engine_row_label(r)
            if label not in labels:
                labels.append(label)
        parts.append(f"{driver or 'unrecorded'} ({', '.join(labels)} rows)")
    return (
        gpu,
        " / ".join(parts)
        + " - separate same-GPU-model sessions, disclosed in bench/FAIRNESS.md"
        ' "Engine configuration"',
    )


def _engine_versions(results: list[dict]) -> list[str]:
    pins = []
    for result in results:
        config = result.get("engine_config", {})
        bits = [result["engine"]]
        for key in ("vllm_version", "module_version", "build_info", "version"):
            if config.get(key):
                bits.append(str(config[key]))
        pin = " ".join(bits)
        if pin not in pins:
            pins.append(pin)
    return pins


def _measurement_dates(results: list[dict]) -> list[str]:
    """Sorted unique UTC dates (YYYY-MM-DD) taken from the results files' own
    ``environment.timestamp_utc``; empty when no file carries a parsable
    timestamp (the footnote then says the date is unrecorded).

    >>> _measurement_dates([{"environment": {"timestamp_utc": "2026-07-10T01:01:33+00:00"}}])
    ['2026-07-10']
    >>> _measurement_dates([{"environment": {"timestamp_utc": None}}, {"environment": {}}])
    []
    """
    dates = set()
    for result in results:
        stamp = result.get("environment", {}).get("timestamp_utc")
        if isinstance(stamp, str) and re.match(r"^\d{4}-\d{2}-\d{2}T", stamp):
            dates.add(stamp[:10])
    return sorted(dates)


