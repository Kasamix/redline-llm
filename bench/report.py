def build_arg_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="python -m bench.report",
        description="Render committed benchmark results JSON into markdown "
        "(no number appears anywhere that is not generated from a results file).",
    )
    parser.add_argument(
        "--results",
        nargs="+",
        metavar="PATH",
        help="result JSON files and/or directories to render",
    )
    parser.add_argument("--out", help="write the markdown here instead of stdout")
    parser.add_argument(
        "--write-readme",
        metavar="README",
        help="splice the table between the BENCH_RESULTS markers of this file",
    )
    parser.add_argument(
        "--check",
        nargs="+",
        metavar="PATH",
        help="audit results files/directories and exit non-zero on "
        "violations: schema; forced lengths and all metrics recomputed from "
        "the committed per-token data (a truncated token list or an edited "
        "median fails even with green self-reported flags); summary/median/"
        "dispersion re-derivation; duplicate engine/case pairs; ambiguous "
        "row labels; environment consistency",
    )
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_arg_parser().parse_args(argv)
    if args.check and (args.results or args.out or args.write_readme):
        raise SystemExit("--check runs alone")
    if args.check:
        return check_results(args.check)
    if not args.results:
        raise SystemExit("pass --results PATH... (or --check PATH...)")

    results = load_results(args.results)
    block = render_markdown(results)
    if args.write_readme:
        splice_readme(args.write_readme, block)
        print(f"[report] wrote results table into {args.write_readme}")
    if args.out:
        Path(args.out).write_text(block, encoding="utf-8")
        print(f"[report] wrote {args.out}")
    elif not args.write_readme:
        print(block)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
