README_END = "<!-- BENCH_RESULTS:END -->"

CASE_ORDER = ("primary", "arrival", "longout", "batch1")
ENGINE_ORDER = ("redline", "vllm", "vllm024", "llamacpp")

# Columns: (results key, header). The headline column label states that the
